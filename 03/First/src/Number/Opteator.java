package Number;

public class Opteator {

	public static void main(String[] args) {
		//3.4 运算符
		int a = 10;
		int b = 20;
		int c = a + b;
		System.out.println("c的值为" + c);
		
		
		int x, y, z;
		x = 4;
		z = y = x + 20;		//当等式中有两个等号存在时，会从右侧的等号开始计算。
		System.out.println("x, y, z的值分别为 " + x + ", " + y + ", " + z);
	}

}
